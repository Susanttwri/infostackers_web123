document.addEventListener("DOMContentLoaded", function() {
    var coll = document.getElementsByClassName("collapsible");

    // Initially open the first section and close others
    for (var i = 0; i < coll.length; i++) {
        var content = coll[i].nextElementSibling;
        if (i === 0) {
            coll[i].classList.add("active");
            content.style.display = "block"; // First section is open
            coll[i].innerHTML = coll[i].innerHTML + ' <span class="icon">➖</span>'; // Add '-' symbol
        } else {
            content.style.display = "none"; // All other sections are closed
            coll[i].innerHTML = coll[i].innerHTML + ' <span class="icon" style="">➕</span>'; // Add '+' symbol
        }
    }

    // Add click event listeners to toggle the sections
    for (var i = 0; i < coll.length; i++) {
        coll[i].addEventListener("click", function() {
            var content = this.nextElementSibling;

            // Close all other sections
            for (var j = 0; j < coll.length; j++) {
                if (coll[j] !== this) {
                    coll[j].classList.remove("active");
                    coll[j].nextElementSibling.style.display = "none";
                    coll[j].querySelector(".icon").innerHTML = "➕"; // Set symbol to '+'
                }
            }

            // Toggle the clicked section
            this.classList.toggle("active");
            if (content.style.display === "block") {
                content.style.display = "none";
                this.querySelector(".icon").innerHTML = "➕"; // Set symbol to '+'
            } else {
                content.style.display = "block";
                this.querySelector(".icon").innerHTML = "➖"; // Set symbol to '-'
            }
        });
    }
});
